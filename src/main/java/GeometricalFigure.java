/**
 * Это интерфейс геометрической фигуры. Любой класс(круг, квадрат, треугольник и т.д. должен реализовать этот интерфейс.
 */
public interface GeometricalFigure {
    /**
     * @return double(периметр фигуры)
     */
    double getPerimeter();
    /**
     * @return double(площадь фигуры)
     */
    double getSquare();
}
