/**
 * Интерфейс для нахождения радиуса и площади окружности, вписанной в фигуру.
 */
public interface InscribedCircle {

    /**
     * @return double(радиус вписанной окружности)
     */
    double getROfInscribedCircle();

    /**
     * @return double(площадь вписанной окружности)
     */
    double getSquareOfInscribedCicrle();
}
